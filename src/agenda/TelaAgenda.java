    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        agenda.Contato selecionado = listaContatos.getSelectedValue();

        if (selecionado != null) {
            int resposta = javax.swing.JOptionPane.showConfirmDialog(this, "Tem certeza que quer apagar?", "Excluir", javax.swing.JOptionPane.YES_NO_OPTION);

            if (resposta == javax.swing.JOptionPane.YES_OPTION) {
                agenda.AgendaDAO dao = new agenda.AgendaDAO();
                dao.remover(selecionado.getId());
                atualizarListaContatos();
                javax.swing.JOptionPane.showMessageDialog(this, "Excluído!");
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Selecione um contato para excluir.");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

