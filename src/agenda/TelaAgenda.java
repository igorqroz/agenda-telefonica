private void atualizarListaContatos() {
        agenda.AgendaDAO dao = new agenda.AgendaDAO();
        modeloLista.clear();
        for (agenda.Contato c : dao.listarTodos()) {
            modeloLista.addElement(c);

        }
    }

    private void limparCampos() {
        txtNome.setText("");
        txtTelefone.setText("");
        txtEmail.setText("");

        // Campos específicos
        txtNiver.setText("");
        txtEndereco.setText("");
        txtEmpresa.setText("");
        txtCargo.setText("");

        // Limpa a seleção dos botões de Pessoal/Profissional
        buttonGroup1.clearSelection();

        // Reseta o ID para garantir que o próximo clique em salvar crie um NOVO
        idEmEdicao = -1;
    }
